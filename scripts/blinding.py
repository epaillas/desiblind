def resolve_products(args):
    """Resolve config, fit root, and options from explicit config or fit selectors."""
    fit_args = clear_eval_kmax_overrides(args)
    if args.config_fn is not None:
        config_fn = args.config_fn
        if not config_fn.exists():
            raise FileNotFoundError(f'Config file does not exist: {config_fn}')
        options = full_shape_tools.read_options(config_fn)
        fit_dir = config_fn.parent
        fits_dir = fit_dir.parent
        return options, fits_dir, config_fn

    options = build_options_from_args(fit_args)
    candidates = [fit_args.fits_dir / fit_args.dataset, fit_args.fits_dir]
    for fits_dir in candidates:
        config_fn = get_fit_fn(options, fits_dir=fits_dir, kind='config', ext='yaml')
        profiles_fn = get_fit_fn(options, fits_dir=fits_dir, kind='profiles')
        if config_fn.exists() or profiles_fn.exists():
            if config_fn.exists():
                options = full_shape_tools.read_options(config_fn)
            return options, fits_dir, config_fn

    checked = [str(get_fit_fn(options, fits_dir=fits_dir, kind='config', ext='yaml')) for fits_dir in candidates]
    raise FileNotFoundError(
        'Could not resolve full_shape products. Checked config paths: '
        f'{checked}. Provide --config-fn or matching fit selectors.'
    )


def get_product_path(options, fits_dir, kind, config_fn=None, ichain=None, ext='npy'):
    """Resolve a fit product path for either a fit root or an explicit fit directory."""
    if config_fn is not None:
        fit_dir = config_fn.parent
        if kind == 'config':
            return config_fn
        if kind == 'profiles':
            return fit_dir / 'profiles.npy'
        if kind == 'chain':
            if ichain is None:
                raise ValueError('ichain must be provided when resolving chain products.')
            return fit_dir / f'chain_{ichain}.npy'
    return get_fit_fn(options, fits_dir=fits_dir, kind=kind, ichain=ichain, ext=ext)


def load_bestfit(options, fits_dir, config_fn=None):
    """Load the best-fit parameter dictionary from full_shape profiles."""
    profiles_fn = get_product_path(options, fits_dir=fits_dir, kind='profiles', config_fn=config_fn)
    if not profiles_fn.exists():
        raise FileNotFoundError(
            f'No profile found at {profiles_fn}. Run the corresponding full_shape profile job first.'
        )
    profiles = Profiles.load(profiles_fn)
    bestfit = profiles.bestfit.choice(index='argmax', input=True)
    return {name: float(value) for name, value in bestfit.items()}


def load_chain(options, fits_dir, chain_slice=None, config_fn=None):
    """Concatenate available full_shape chain files."""
    if chain_slice is not None:
        start, stop = chain_slice
        chain_fns = [
            get_product_path(options, fits_dir=fits_dir, kind='chain', ichain=ichain, config_fn=config_fn)
            for ichain in range(start, stop)
        ]
    else:
        chain_dir = get_product_path(options, fits_dir=fits_dir, kind='chain', ichain=0, config_fn=config_fn).parent
        chain_fns = sorted(chain_dir.glob('chain*.npy'))
    if not chain_fns:
        raise FileNotFoundError(
            f'No chain files found under {fits_dir}. Run the corresponding full_shape sampling job first.'
        )
    missing = [str(fn) for fn in chain_fns if not fn.exists()]
    if missing:
        raise FileNotFoundError(
            f'Missing full_shape chain files: {missing}. Run the corresponding full_shape sampling job first.'
        )
    return Chain.concatenate([Chain.load(fn).remove_burnin(0.1) for fn in chain_fns])


def get_sampled_cosmo_param_names(chain):
    """Return cosmological parameters present in the saved full_shape chain."""
    names = list(chain.params(input=True).keys())
    preferred = [name for name in PREFERRED_COSMO_PARAMS if name in names]
    if preferred:
        return preferred
    inferred = [name for name in names if '.' not in name]
    if inferred:
        return inferred
    raise ValueError('Could not identify cosmological parameters in the saved full_shape chain.')


def tracerz_from_catalog(tracer, zrange):
    """Translate a catalog tracer + zrange back to a full_shape tracer-bin label."""
    mapping = full_shape_tools.get_full_tracer_zrange(None)
    if isinstance(tracer, (list, tuple)):
        if isinstance(zrange[0], (list, tuple)):
            zranges = zrange
        else:
            zranges = [zrange] * len(tracer)
        return 'x'.join(tracerz_from_catalog(subtracer, subzrange) for subtracer, subzrange in zip(tracer, zranges, strict=True))
    for tracerz, candidate in mapping.items():
        if tracerz.startswith(str(tracer)) and np.allclose(candidate, zrange):
            return tracerz
    return str(tracer)


