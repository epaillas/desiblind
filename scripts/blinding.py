def get_plot_coordinates(state, label):
    """Return plotting coordinates and scaling matching the native desilike observable plotters."""
    data_pole = state['observable'].data.get(**label)
    if state['stat'] == 'mesh2_spectrum':
        x = data_pole.coords('k')
        scale = x
        xlabel = r'$k$ [$h/\mathrm{Mpc}$]'
        ylabel = r'$k P_{\ell}(k)$ [$(\mathrm{Mpc}/h)^{2}$]'
        residual = rf'$\Delta P_{{{label["ells"]}}} / \sigma_{{ P_{{{label["ells"]}}} }}$'
        return x, scale, xlabel, ylabel, residual

    if state['stat'] == 'mesh3_spectrum':
        if 'scoccimarro' in data_pole.basis:
            x = np.arange(data_pole.size)
            scale = data_pole.coords('k').prod(axis=-1)
            xlabel = r'$k$ triangle index'
            ylabel = r'$k_1 k_2 k_3 B_{\ell}(k_1, k_2, k_3)$ [$(\mathrm{Mpc}/h)^3$]'
        elif 'sugiyama' in data_pole.basis:
            k = data_pole.coords('k')
            scale = k.prod(axis=-1)
            if np.allclose(k[..., 1], k[..., 0]):
                x = k[..., 0]
                xlabel = r'$k$ [$h/\mathrm{Mpc}$]'
            else:
                x = np.arange(data_pole.size)
                xlabel = r'$k$ triangle index'
            ylabel = r'$k^2 B_{\ell}(k, k)$ [$(\mathrm{Mpc}/h)^4$]'
        else:
            raise ValueError(f'Unsupported bispectrum basis: {data_pole.basis}')
        residual = rf'$\Delta B_{{{label["ells"]}}} / \sigma_{{ B_{{{label["ells"]}}} }}$'
        return x, scale, xlabel, ylabel, residual

    raise ValueError(f'Unsupported observable for plotting: {state["stat"]}')


def blend_color(color, target='black', amount=0.35):
    """Blend a matplotlib color toward a target color."""
    rgb = np.array(mcolors.to_rgb(color))
    target_rgb = np.array(mcolors.to_rgb(target))
    return tuple((1.0 - amount) * rgb + amount * target_rgb)


def get_multipole_plot_styles(state, variant):
    """Return one plotting style per multipole label."""
    labels = state['observable'].data.labels()
    styles = []
    for ill, _ in enumerate(labels):
        base_color = f'C{ill:d}'
        if variant == 'reference':
            styles.append({
                'color': blend_color(base_color, target='black', amount=0.25),
                'lw': 1.8,
                'alpha': 1.0,
                'label': 'reference' if ill == 0 else None,
            })
        elif variant == 'shifted':
            styles.append({
                'color': blend_color(base_color, target='white', amount=0.2),
                'lw': 0.9,
                'alpha': 0.2,
                'label': 'shifted' if ill == 0 else None,
            })
        else:
            raise ValueError(f'Unknown plot style variant: {variant}')
    return styles


def normalize_plot_styles(labels, kw_theory):
    """Broadcast one style or trim repeated labels to match the plotted leaves."""
    if kw_theory is None:
        kw_theory = {}
    if isinstance(kw_theory, dict):
        return [kw_theory] * len(labels)
    if len(kw_theory) != len(labels):
        return [{key: value for key, value in kw_theory[0].items() if (key != 'label') or (ill == 0)} for ill in range(len(labels))]
    return kw_theory


def build_shift_plot_figure(state):
    """Build a theory-only figure with one main panel and one residual panel per leaf."""
    labels = state['observable'].data.labels()
    fig, axes = plt.subplots(
        len(labels) + 1,
        1,
        figsize=(6.0, 2.4 + 1.6 * len(labels)),
        gridspec_kw={'height_ratios': [3.0] + [1.2] * len(labels), 'hspace': 0.05},
        squeeze=False,
    )
    axes = axes[:, 0]
    reference_styles = get_multipole_plot_styles(state, variant='reference')
    main_ylabel = None
    xlabel = None

    for ill, label in enumerate(labels):
        reference_pole = state['reference'].get(**label)
        x, scale, xlabel, ylabel, residual_ylabel = get_plot_coordinates(state, label)
        if main_ylabel is None:
            main_ylabel = ylabel
        axes[0].plot(x, scale * reference_pole.value(), **reference_styles[ill])
        axes[ill + 1].axhline(0., color='0.75', lw=0.8, zorder=0)
        axes[ill + 1].set_ylabel(residual_ylabel)
        if ill < len(labels) - 1:
            axes[ill + 1].tick_params(labelbottom=False)

    axes[0].set_ylabel(main_ylabel)
    axes[0].tick_params(labelbottom=False)
    if xlabel is not None:
        axes[-1].set_xlabel(xlabel)
    if any(style.get('label') for style in reference_styles):
        axes[0].legend()
    fig.align_ylabels(axes)
    return fig


def overlay_shifted_theory(state, fig, theory_observable, kw_theory=None):
    """Overlay one shifted theory and residual relative to the reference theory."""
    labels = state['observable'].data.labels()
    kw_theory = normalize_plot_styles(labels, kw_theory)
    axes = fig.axes
    for ill, label in enumerate(labels):
        reference_pole = state['reference'].get(**label)
        theory_pole = theory_observable.get(**label)
        std = state['observable'].covariance.at.observable.get(**label).std()
        x, scale, _, _, residual_ylabel = get_plot_coordinates(state, label)
        style = kw_theory[ill]
        axes[0].plot(x, scale * theory_pole.value(), **style)
        axes[ill + 1].plot(x, (reference_pole.value() - theory_pole.value()) / std, **style)
        axes[ill + 1].set_ylabel(residual_ylabel)
    if any(style.get('label') for style in kw_theory):
        axes[0].legend()


def save_blinded_plot(state, blinder, output_dir, num_samples):
    """Save shifted realizations using a theory-only reference and theory residuals."""
    blinder_observable = next(observable for observable in blinder.observables if observable.name == state['output_name'])
    fig = build_shift_plot_figure(state)
    for index, blinded in enumerate(blinder_observable.blinded_data[:num_samples]):
        shifted_styles = get_multipole_plot_styles(state, variant='shifted')
        if index > 0:
            shifted_styles = [{**style, 'label': None} for style in shifted_styles]
        overlay_shifted_theory(
            state,
            fig,
            blinded,
            kw_theory=shifted_styles,
        )
    fig.savefig(output_dir / f'blinding_shifts_{state["plot_name"]}.png', dpi=300)
    plt.close(fig)


def write_combined_shifts(blinders, save_dir):
    """Write all blinded shifts to a single shifts_blinding.npy file."""
    output = {}
    for blinder in blinders.values():
        for observable in blinder.observables:
            if len(observable.blinded_data) < blinder.blinded_nmax:
                raise ValueError(f'Please generate at least {blinder.blinded_nmax} blinded realizations for {observable.name}.')
            for bid, blinded_data in enumerate(observable.blinded_data):
                shifts = {}
                for ell in blinded_data.ells:
                    pole = blinded_data.get(ells=ell)
                    reference = observable.reference_data.get(ells=ell)
                    shifts[ell] = (pole.coords('k'), pole.value() - reference.value())
                key = hashlib.sha256(f'{observable.name}_bid{bid}'.encode()).hexdigest()
                output[key] = shifts
    save_fn = Path(save_dir) / SHIFTS_FILENAME
    save_fn.parent.mkdir(parents=True, exist_ok=True)
    np.save(save_fn, output)
    return save_fn


def choose_demo_name(states, requested_name=None):
    """Pick a valid power-spectrum public name for apply/remove validation."""
    power_names = [state['public_name'] for state in states if state['stat'] == 'mesh2_spectrum']
    if not power_names:
        return None
    if requested_name is None:
        return power_names[0]
    requested = normalize_canonical_tracerbin_name(requested_name)
    if requested in power_names:
        return requested
    raise ValueError(f'Could not match --demo-name={requested_name} to any saved power-spectrum observable: {power_names}')


def run_apply_remove_demo(name, shifts_dir, state):
    """Validate the public PK apply/remove API on one saved power-spectrum observable."""
    observable = state['data']
    poles = [observable.get(ell) for ell in observable.ells]
    k = poles[0].coords('k')
    values = [pole.value() for pole in poles]
    blinded_data = TracerPowerSpectrumMultipolesBlinder.apply_blinding(
        name=name,
        k=k,
        data=values,
        ells=observable.ells,
        save_dir=shifts_dir,
    )
    blinded_tree = TracerPowerSpectrumMultipolesBlinder.apply_blinding(
        name=name,
        data=observable,
        save_dir=shifts_dir,
    )
    if not np.allclose(blinded_tree.value(), np.ravel(blinded_data)):
        raise RuntimeError('ObservableTree and array blinding results do not match.')
    unblinded_data = TracerPowerSpectrumMultipolesBlinder.remove_blinding(
        name=name,
        k=k,
        data=blinded_data,
        ells=observable.ells,
        save_dir=shifts_dir,
        force=True,
    )
    if not np.allclose(np.ravel(unblinded_data), np.ravel(values)):
        raise RuntimeError('remove_blinding did not recover the original data vector.')


def main():
    args = parse_args()
    if not args.skip_write_shifts and args.num_samples < TracerPowerSpectrumMultipolesBlinder.blinded_nmax:
        raise ValueError(
            f'--num-samples must be at least {TracerPowerSpectrumMultipolesBlinder.blinded_nmax} when writing blinded shifts.'
        )
    if args.skip_write_shifts and not args.skip_demo_apply_remove:
        raise ValueError('Cannot run the apply/remove demo when --skip-write-shifts is set.')

    args.plot_dir.mkdir(parents=True, exist_ok=True)
    args.shifts_dir.mkdir(parents=True, exist_ok=True)
    args.cache_dir.mkdir(parents=True, exist_ok=True)

    fit_options, fits_dir, config_fn = resolve_products(args)
    options = apply_runtime_overrides(copy.deepcopy(fit_options), args)
    bestfit = load_bestfit(fit_options, fits_dir=fits_dir, config_fn=config_fn)
    chain = load_chain(fit_options, fits_dir=fits_dir, chain_slice=args.chain_slice, config_fn=config_fn)
    plotting_likelihood, states = build_states(options, cache_dir=args.cache_dir, cache_mode=args.cache_mode)
    validate_eval_kmax(states, get_eval_kmax_by_stat(args))

    blinders = {}
    for state in states:
        blinders.setdefault(state['stat'], get_blinder_for_stat(state['stat']))

    plotting_likelihood(**bestfit)
    for state in states:
        reference = clone_theory_as_observable(state['observable'])
        state['reference'] = reference
        blinders[state['stat']].add_observable(name=state['public_name'], data=reference, covariance=state['covariance'])
        save_reference_plot(state, args.plot_dir)

    sampled_cosmo = get_sampled_cosmo_param_names(chain)
    samples = sample_from_gaussian(
        mean=[bestfit[name] for name in sampled_cosmo],
        covariance=chain.covariance(sampled_cosmo) * 4,
        size=args.num_samples,
        seed=args.seed,
    )
    for sample in samples:
        sample_params = get_chain_param_dict(bestfit, chain, sample)
        plotting_likelihood(**sample_params)
        for state in states:
            blinded = clone_theory_as_observable(state['observable'])
            blinders[state['stat']].set_blinded_data(state['public_name'], blinded_data=blinded)

    for state in states:
        save_blinded_plot(state, blinders[state['stat']], args.plot_dir, args.num_samples)

    if not args.skip_write_shifts:
        write_combined_shifts(blinders, save_dir=args.shifts_dir)

    if not args.skip_demo_apply_remove:
        demo_name = choose_demo_name(states, requested_name=args.demo_name)
        if demo_name is not None:
            demo_state = next(state for state in states if state['public_name'] == demo_name and state['stat'] == 'mesh2_spectrum')
            run_apply_remove_demo(demo_name, shifts_dir=args.shifts_dir, state=demo_state)


if __name__ == '__main__':
    main()
