def get_eval_kmax_by_stat(args):
    """Return the requested evaluation kmax per statistic."""
    return resolve_eval_kmax_overrides(
        eval_kmax=args.eval_kmax,
        eval_kmax_mesh2=args.eval_kmax_mesh2,
        eval_kmax_mesh3=args.eval_kmax_mesh3,
    )


def apply_runtime_overrides(options, args, include_eval_kmax=True):
    """Apply CLI overrides to options loaded from config.yaml."""
    options = full_shape_tools.fill_fiducial_options(options)
    if include_eval_kmax:
        options = apply_eval_kmax_override(options, get_eval_kmax_by_stat(args))
    for likelihood in options['likelihoods']:
        covariance = likelihood.get('covariance', {})
        if args.stats_dir is not None:
            covariance['stats_dir'] = str(args.stats_dir)
        for observable in likelihood['observables']:
            observable['catalog']['stats_dir'] = str(args.stats_dir)
            for select in observable['stat'].get('select', []):
                if isinstance(select.get('ells'), list):
                    select['ells'] = tuple(select['ells'])
            if args.theory_model is not None:
                if observable['stat']['kind'] == 'mesh3_spectrum' and args.theory_model == 'reptvelocileptors':
                    raise ValueError('reptvelocileptors is not supported for mesh3_spectrum.')
                observable['theory']['model'] = args.theory_model
    return options


def normalize_tracer(tracer):
    """Translate legacy tracer aliases to full_shape tracer-bin labels."""
    return to_tracerbin_name(tracer)


def resolve_tracers(tracers):
    if tracers is None:
        return ['LRG1']
    return normalize_tracer_names(tracers, output='tracerbin')


def build_options_from_args(args):
    """Construct full_shape options when config.yaml is not provided."""
    likelihoods = []
    for tracer in resolve_tracers(args.tracers):
        likelihood = full_shape_tools.generate_likelihood_options_helper(
            stats=args.stats,
            tracer=tracer,
            region=args.region,
            version=args.dataset,
            covariance=args.covariance_version,
            stats_dir=args.stats_dir,
        )
        if args.theory_model is not None:
            for observable_options in likelihood['observables']:
                if observable_options['stat']['kind'] == 'mesh3_spectrum' and args.theory_model == 'reptvelocileptors':
                    raise ValueError('reptvelocileptors is not supported for mesh3_spectrum.')
                observable_options['theory']['model'] = args.theory_model
        likelihoods.append(likelihood)
    return apply_runtime_overrides({
        'likelihoods': likelihoods,
        'cosmology': {'template': args.template, 'model': args.cosmology_model},
    }, args)


